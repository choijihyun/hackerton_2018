package com.homeworkNotice.dto;

public class PassDto {

	private int auto;
	private int year;
	private int orderExam;
	private int cutLine;
	private int passNum;
	private int examNum;
	
	
	public int getAuto() {
		return auto;
	}
	public void setAuto(int auto) {
		this.auto = auto;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getOrderExam() {
		return orderExam;
	}
	public void setOrderExam(int orderExam) {
		this.orderExam = orderExam;
	}
	public int getCutLine() {
		return cutLine;
	}
	public void setCutLine(int cutLine) {
		this.cutLine = cutLine;
	}
	public int getPassNum() {
		return passNum;
	}
	public void setPassNum(int passNum) {
		this.passNum = passNum;
	}
	public int getExamNum() {
		return examNum;
	}
	public void setExamNum(int examNum) {
		this.examNum = examNum;
	}
	
	
	
	
}
